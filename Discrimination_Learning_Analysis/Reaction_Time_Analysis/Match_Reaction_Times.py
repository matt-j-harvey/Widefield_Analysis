import numpy as np
import matplotlib.pyplot as plt
import os


def get_specific_reaction_times(session_list, window=[900, 1000]):

    trial_count = 0
    # Iterate Through Session List
    for base_directory in session_list:

        # Create Lists To Hold Stimuli Onsets
        matched_reaction_times_lick_onsets = []
        matched_reaction_times_stimuli_onsets = []

        # Load Behaviour Matrix
        behaviour_matrix = np.load(os.path.join(base_directory, "Stimuli_Onsets", "Behaviour_Matrix.npy"), allow_pickle=True)

        # Iterate Through Trial
        for trial in behaviour_matrix:

            # Get Trial Behavioural Characteristics
            trial_type = trial[1]
            correct = trial[3]
            reaction_time = trial[23]

            # Get Event Onsets
            onset_frame = trial[18]
            lick_frame = trial[22]

            if trial_type == 1 and correct == 1:

                if onset_frame != None and lick_frame != None:
                    if onset_frame > 3000:

                        if reaction_time > window[0] and reaction_time < window[1]:
                            matched_reaction_times_stimuli_onsets.append(onset_frame)
                            matched_reaction_times_lick_onsets.append(lick_frame)
                            trial_count += 1

        np.save(os.path.join(base_directory, "Stimuli_Onsets", "Stimuli_Onsets_Matched_Reaction_Times_" + str(window[0]) + "_" + str(window[1]) + ".npy"), matched_reaction_times_stimuli_onsets)
        np.save(os.path.join(base_directory, "Stimuli_Onsets", "Lick_Onsets_Matched_Reaction_Times_" + str(window[0]) + "_" + str(window[1]) + ".npy"), matched_reaction_times_stimuli_onsets)
        print("base directory", base_directory, "trials :", len(matched_reaction_times_stimuli_onsets))
    print("Trial Count", trial_count)


