def split_onsets_by_reaction_time(session_list, fast_window=[500, 1000], slow_window=[1500,2000]):

    # Iterate Through Session List
    for base_directory in session_list:

        # Create Lists To Hold Stimuli Onsets
        fast_reaction_stimuli_onsets = []
        slow_reaction_stimuli_onsets = []

        fast_reaction_lick_onsets = []
        slow_reaction_lick_onsets = []

        # Load Behaviour Matrix
        behaviour_matrix = np.load(os.path.join(base_directory, "Stimuli_Onsets", "Behaviour_Matrix.npy"), allow_pickle=True)

        #Iterate Through Trial
        for trial in behaviour_matrix:

            # Get Trial Behavioural Characteristics
            trial_type = trial[1]
            correct = trial[3]
            reaction_time = trial[23]

            # Get Event Onsets
            onset_frame = trial[18]
            lick_frame = trial[22]

            if trial_type == 1 and correct == 1:

                if onset_frame != None and lick_frame != None:
                    if onset_frame > 3000:

                        if reaction_time > fast_window[0] and reaction_time < fast_window[1]:
                            fast_reaction_stimuli_onsets.append(onset_frame)
                            fast_reaction_lick_onsets.append(lick_frame)

                        elif reaction_time > slow_window[0] and reaction_time < slow_window[1]:
                            slow_reaction_stimuli_onsets.append(onset_frame)
                            slow_reaction_lick_onsets.append(lick_frame)

        # save Onsets
        save_directory = os.path.join(base_directory, "Stimuli_Onsets")
        np.save(os.path.join(save_directory, "fast_reaction_stimuli_onsets.npy"), fast_reaction_stimuli_onsets)
        np.save(os.path.join(save_directory, "slow_reaction_stimuli_onsets.npy"), slow_reaction_stimuli_onsets)
        np.save(os.path.join(save_directory, "fast_reaction_lick_onsets.npy"), fast_reaction_lick_onsets)
        np.save(os.path.join(save_directory, "slow_reaction_lick_onsets.npy"), slow_reaction_lick_onsets)

        print("Session: ", base_directory, "Fast Reaction Trials:", len(fast_reaction_stimuli_onsets), "Slow Reaction Trials:", len(slow_reaction_stimuli_onsets))




